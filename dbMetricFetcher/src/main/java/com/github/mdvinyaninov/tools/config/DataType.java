package com.github.mdvinyaninov.tools.config;

/**
 * DataTypes
 */
public enum DataType {
    BOOLEAN,
    /**
     * int 32 bit, i.e java int
     */
    INTEGER,
    /**
     * int 64 bit, i.e java long
     */
    BIGINTEGER,
    /**
     * int 64 bit, i.e java long
     */
    BIGDECIMAL,
    /**
     * float 64 bit, i.e java double
     */
    FLOAT,
    TEXT,
    /**
     * unix nanos
     */
    TIMESTAMP
}